	<p>
		Studieren mit Pflegeverantwortung bedeutet, dass Studierende neben ihrem Studium auch für die
		Betreuung und Pflege von Kindern oder Familienangehörigen verantwortlich sind. Diese
		Pflegeverantwortung kann sowohl die Versorgung eigener Kinder als auch die Unterstützung von
		pflegebedürftigen Personen umfassen. Wer über mehrere Monate täglich oder mehrfach in der Woche
		(informelle) Pflege leistet, gilt als pflegende Person. Informelle Pflege umfasst beispielsweise
		Haushaltstätigkeiten wie Putzen, Einkaufen oder die Kommunikation mit Krankenkassen.
	</p>
